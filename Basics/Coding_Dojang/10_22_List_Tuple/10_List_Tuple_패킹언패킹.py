## 연습문제 22.9
# 입력값 2개 받고
# 첫번째 정수부터 두번째 정수까지를 지수로하는 2의 거듭제곱 리스트 출력
# 두번쨰, 뒤에서 두번째 요소 삭제

a, b = map(int, input().split())
integers = list(range(a, b+1))
sqrs = []
del integers[1]
del integers[-2]

for i in integers :
    sqrs.append(2**i)

print(sqrs)
